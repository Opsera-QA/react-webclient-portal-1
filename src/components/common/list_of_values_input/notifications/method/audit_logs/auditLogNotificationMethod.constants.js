export const AUDIT_LOG_NOTIFICATION_METHODS = {
  PIPELINE: "pipeline",
  TOOL_REGISTRY: "registry",
  TASK: "task",
};

export const AUDIT_LOG_NOTIFICATION_METHOD_LABELS = {
  PIPELINE: "Pipeline",
  TOOL_REGISTRY: "Tool",
  TASK: "Task",
};

export const AUDIT_LOG_NOTIFICATION_METHOD_SELECT_OPTIONS = [
  {
    value: AUDIT_LOG_NOTIFICATION_METHODS.PIPELINE,
    text: AUDIT_LOG_NOTIFICATION_METHOD_LABELS.PIPELINE
  },
  {
    value: AUDIT_LOG_NOTIFICATION_METHODS.TASK,
    text: AUDIT_LOG_NOTIFICATION_METHOD_LABELS.TASK
  },
  {
    value: AUDIT_LOG_NOTIFICATION_METHODS.TOOL_REGISTRY,
    text: AUDIT_LOG_NOTIFICATION_METHOD_LABELS.TOOL_REGISTRY
  },
];

export const getNotificationMethodLabel = (notificationMethod) => {
  switch (notificationMethod) {
    case AUDIT_LOG_NOTIFICATION_METHODS.PIPELINE:
      return AUDIT_LOG_NOTIFICATION_METHOD_LABELS.PIPELINE;
    case AUDIT_LOG_NOTIFICATION_METHODS.TASK:
      return AUDIT_LOG_NOTIFICATION_METHOD_LABELS.TASK;
    case AUDIT_LOG_NOTIFICATION_METHODS.TOOL_REGISTRY:
      return AUDIT_LOG_NOTIFICATION_METHOD_LABELS.TOOL_REGISTRY;
    default:
      return notificationMethod;
  }
};